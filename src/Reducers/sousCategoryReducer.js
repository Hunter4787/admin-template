  {
    console.log(action);
    switch(action.type)
    {
            case 'ADD_SOUSCATEGORY' :
            return (
                state.concat(action.newcategory)
            )
            case 'EDIT_SOUSCATEGORY':
            return (
                state.map(el=>el.id_sous_categorie===action.editsouscategory.id_sous_categorie? el=action.editsouscategory:el)
            )
            case 'REMOVE_SOUSCATEGORY':
            return (
              state.filter(el=>el.id_sous_categorie!==action._id)
            )
        default :
        return state
    }
  }
